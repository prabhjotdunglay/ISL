    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validation($request);
        $division = new Division();
        $division->name = $request->name;
        
       // $accessory->status = $request->status;
        $division->status = $request->input('status') ? 1 : 0;
        $division->remark = $request->remark;

        $division->save();
        $division->code ='DM000'. $division->id;
        $division->save();
        
        return redirect("/division/create")->with('Status', 'SUCCESS: You have added new division.');
    }

    public function validation($request){
