     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validation($request);
        $transport = new Transport();
        $transport->name = $request->name;
        
       // $accessory->status = $request->status;
        $transport->status = $request->input('status') ? 1 : 0;
        $transport->remark = $request->remark;
        $transport->save();
        $transport->code ='TM000'. $transport->id;
        $transport->save();
        
        return redirect("/transport/create")->with('Status', 'SUCCESS: You have added new transport.');
    }
    public function validation($request){
