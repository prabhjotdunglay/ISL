    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validation($request);
        $make = new Make();
        $make->name = $request->name;
        
       // $accessory->status = $request->status;
        $make->status = $request->input('status') ? 1 : 0;
        $make->remark = $request->remark;
        $make->save();
        $make->code ='MK000'. $make->id;
        $make->save();
        
        return redirect("/make/create")->with('Status', 'SUCCESS: You have added new make.');
    }

    public function validation($request){
        return $this->validate($request, [
            'name' => 'required|max:255',
            'status' => 'required',
