    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validation($request);
        $hour = new Hour();
        $hour->name = $request->name;
        
       // $accessory->status = $request->status;
        $hour->status = $request->input('status') ? 1 : 0;
        $hour->remark = $request->remark;

        $hour->save();
        $hour->code ='HM000'. $hour->id;
        $hour->save();
        return redirect("/hour/create")->with('Status', 'SUCCESS: You have added new hour.');
    }

    public function validation($request){
